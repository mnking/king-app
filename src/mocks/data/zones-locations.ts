// Helper functions for generating IDs and codes
export const generateId = () =>
  `${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;

export const generateLocationCode = (
  zoneCode: string,
  type: 'RBS' | 'CUSTOM',
  details: {
    rbsRow?: string;
    rbsBay?: string;
    rbsSlot?: string;
    customLabel?: string;
  },
): { locationCode: string; absoluteCode: string; displayCode: string } => {
  if (type === 'RBS') {
    const locationCode = `${details.rbsRow}${details.rbsBay}${details.rbsSlot}`;
    const absoluteCode = `${zoneCode}${locationCode}`;
    const displayCode = `${zoneCode}-${details.rbsRow}-${details.rbsBay}-${details.rbsSlot}`;
    return { locationCode, absoluteCode, displayCode };
  } else {
    const locationCode = details.customLabel!;
    const absoluteCode = `${zoneCode}${locationCode}`;
    const displayCode = `${zoneCode}-${locationCode}`;
    return { locationCode, absoluteCode, displayCode };
  }
};
